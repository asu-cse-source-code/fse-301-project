              trim
            ></b-form-input>
          </b-card>
        </b-col>
      </b-row>
      <b-card v-if="allTodos !== undefined" class="mt-5">
        <b-row>
          <b-col>
            <div v-if="user === ''">
              <h1>All Todos</h1>
              <br />
              <div v-for="todos in allTodos" :key="todos._id">
                <h5>{{ todos.username || "AutoGenerated" }}</h5>
