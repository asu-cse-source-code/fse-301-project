              <pre>{{ allTodos }}</pre>
            </div>
          </b-col>
        </b-row>
      </b-card>
      <b-card v-if="allRewards !== undefined" class="mt-5">
        <b-row>
          <b-col>
            <div v-if="user === ''">
              <h1>All Rewards</h1>
              <br />
              <div v-for="Rewards in allRewards" :key="Rewards._id">
                <h5>{{ Rewards.username || "AutoGenerated" }}</h5>
                <pre>{{ Rewards }}</pre>
              </div>
              <br />
            </div>
            <div v-else>
              <h1>{{ user }}'s Rewards</h1>
              <pre>{{ allRewards }}</pre>
            </div>
          </b-col>
        </b-row>
      </b-card>
    </b-container>
    <b-container class="fluid" v-else>
      <b-row>
        <b-col cols="4">
          <div class="message mt-5">You are not authorized.</div>
        </b-col>
        <b-col cols="2"></b-col>
        <b-col cols="6">
          <div class="container">
            <div class="neon">403</div>
            <div class="door-frame">
