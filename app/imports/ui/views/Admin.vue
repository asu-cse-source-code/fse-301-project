                <div class="rectangle"></div>
                <div class="handle"></div>
                <div class="window">
                  <div class="eye"></div>
                  <div class="eye eye2"></div>
                  <div class="leaf"></div>
                </div>
              </div>
            </div>
          </div>
        </b-col>
      </b-row>
    </b-container>
  </div>
</template>

<script>
import Todo from "../../api/collections/Todo";
import Rewards from "../../api/collections/Rewards";

export default {
  data() {
    return {
      user: "",
      admin: ["abspence"],
      authorized: false,
    };
  },
  methods: {
    isAuthorized() {
      if (this.admin.indexOf(this.currentUser?.username) !== -1) {
        return true;
      }
      return false;
    },
  },
  meteor: {
    $subscribe: {
      // Subscribes to the 'threads' publication with no parameters
      allTodos: [],
      allRewards: [],
    },
    allTodos() {
      if (this.user !== "") {
        return Todo.find({ username: this.user }).fetch();
      } else {
        return Todo.find().fetch();
      }
    },
    allRewards() {
      if (this.user !== "") {
        return Rewards.find({ username: this.user }).fetch();
      } else {
        return Rewards.find().fetch();
      }
    },
    currentUser() {
      return Meteor.user();
    },
  },
};
</script>

